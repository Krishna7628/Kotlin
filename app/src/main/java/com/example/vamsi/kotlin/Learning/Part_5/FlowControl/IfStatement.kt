 fun main(args: Array<String>){

     //if-else expression

     val a = 10
     val b = 20
     val c = if(a<b)
         b
     else
         a
     println(c)

     //if-else if-else ladder expression

     val a1 = 10
     val b1 = 20
     val c1 = 30
     val c5 = if(c1<b1)
         b1
     else if(c1<a1)
         a1
     else
         c1
     println(c5)


     //nested if expression


 }